package com.uppfind.util.log;

/**
 * 标点符号常量类
 *
 */
public interface ConstPunctuation {

    /**
     * : 冒号
     */
    String COLON = ":";

    /**
     * - 中划线
     */
    String MINUS = "-";

    /**
     * -- 双中划线
     */
    String TWO_MINUS = "--";

    /**
     * _ 下划线
     */
    String UNDERLINE = "_";

    /**
     * / 斜线
     */
    String SLASH = "/";

    /**
     * , 逗号
     */
    String COMMA = ",";

    /**
     * . 点号
     */
    String DOT = ".";

    /**
     * ; 分号
     */
    String SEMICOLON = ";";

    /**
     * = 等于号
     */
    String EQUAL = "=";

    /**
     * == 恒等于号
     */
    String DOUBLE_EQUAL = "==";

    /**
     * 空格
     */
    String BLANK = " ";

    /**
     * ? 问号
     */
    String QUESTION = "?";

    /**
     * & 连接符
     */
    String LINK = "&";

    /**
     * * 星号
     */
    String ASTERISK = "*";
}
